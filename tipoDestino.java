public class tipoDestino
{
  public tipoDestino(){
  
  } 
  private String vamos;
  public String getVamos(){
    return vamos;
  }
public void irDestino(int escoger){
    switch(escoger){
      case 1:
        vamos = "Asia";
        break;
      case 2:
        vamos = "Norte America";
        break;
      case 3:
        vamos = "Europa";
      case 4:
        vamos = "Australia";
      default:
        vamos = "El destino insertado no existe";
    }
  }
}//final de tipoDestino